# Write an if statment that assigns 0 to the variable b and assigns 1
# to the variable c if the variable a is less than 10

# Retrieve user input for a
a = int(input("What is the value for a? "))
# Initiliaze b and c
b = int
c = int 

if a < 10:
    b = 0
    c = 1
    print("a is: ", a,
          "b is: ", b,
          "c is: ", c)
else:
    print("Pick a number less than 10 to assign b and c values")

